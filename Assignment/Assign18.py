# Problem 1
# Write a python script to print first N Even natural numbers.
'''
N = int(input("Enter the value:"))
i=1
while i<=N:
    print(2*i,end=' ')
    i+=1
'''

# Problem 2
# Write a python script to print first N Even natural numbers in reverse order.
'''
N = int(input("Enter the value:"))
while N:
    print(2*N,end=' ')
    N-=1
'''

