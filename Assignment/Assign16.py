# Problem 1
# Write a python script to print first 10 even natural numbers.
'''
i = 1
while i<=10:
    print(2*i,end=' ')
    i+=1
'''

# Problem 2
# Write a python script to print first 10 even natural numbers in reverse order.
'''
i = 10
while i:
    print(2*i,end=' ')
    i-=1
'''

